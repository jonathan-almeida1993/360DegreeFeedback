package com.mahindra.database.pojo;

public class CompanyPojo extends BasePojo {

	private String companyId;
	private String companyName;
	private String comAdminEmailId;

	public String getCompanyId() {
		return companyId;
	}

	public void setCompanyId(String companyId) {
		this.companyId = companyId;
	}

	public String getCompanyName() {
		return companyName;
	}

	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}

	public String getComAdminEmailId() {
		return comAdminEmailId;
	}

	public void setComAdminEmailId(String comAdminEmailId) {
		this.comAdminEmailId = comAdminEmailId;
	}

}
